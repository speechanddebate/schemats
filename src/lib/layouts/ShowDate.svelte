<script lang='js'>
	import { DateTime } from 'luxon';

	let {
		dt,
		dtISO,
		dtString,
		tz     = 'system',
		format = 'medtext',
		mode   = 'dt',
		spanClass,
		locale,
		showFullTz,
		showTz,
	}                : {
		dt?          : Date,
		dtISO?       : string,
		dtString?    : string,
		showTz?      : boolean,
		showFullTz?  : boolean,
		tz?          : string,
		format?      : string,
		mode?        : string,
		locale?      : string,
		spanClass?   : string,
	}= $props();

	let startDt = DateTime.local();

	if (dt) {
		startDt = DateTime.fromJSDate(dt).setZone(tz);
	} else if (dtISO) {
		startDt = DateTime.fromISO(dtISO).setZone(tz);
	} else if (dtString) {
		startDt = DateTime.fromSQL(dtString).setZone(tz);
	}

	if (locale) {
		startDt = startDt.setLocale(locale);
	}

	let dateOutput = $state('');

	switch (format) {

		case 'iso': // Single Date
			switch (mode) {
				case 'time': // 11:32:00.000-04:00
					dateOutput = startDt.toISOTime();
					break;

				case 'date': // 2017-04-20
					dateOutput = startDt.toISO();
					break;

				default: // 2017-04-20T11:32:00.000-04:00
					dateOutput = startDt.toISODate();
					break;
			}

			break;

		case 'sortable': // Unix Epoch in seconds
			dateOutput = startDt.toUnixInteger();
			break;

		case 'full': // October 14, 1983 at 1:30 PM EDT

			switch (mode) {
				case 'time': // 11:32:00.000-04:00
					dateOutput = startDt.toLocaleString(DateTime.TIME_SIMPLE);
					break;

				case 'date': // 2017-04-20
					dateOutput = startDt.toLocaleString(DateTime.DATE_FULL);
					break;

				default: // 2017-04-20T11:32:00.000-04:00
					dateOutput = startDt.toLocaleString(DateTime.DATETIME_FULL);
					showTz = false;
			}
			break;

		case 'medday': // Fri, Oct 14, 1983, 1:30 PM
			switch (mode) {
				case 'date': // 2017-04-20
					dateOutput = startDt.toLocaleString(DateTime.DATE_MED_WITH_WEEKDAY);
					break;

				default: // 2017-04-20T11:32:00.000-04:00
					dateOutput = startDt.toLocaleString(DateTime.DATETIME_MED_WITH_WEEKDAY);
					showTz = false;
			}
			break;

		case 'med': // Oct 14, 1983, 1:30 PM
			switch (mode) {
				case 'date':
					dateOutput = startDt.toLocaleString(DateTime.DATE_MED);
					break;

				case 'time':
					dateOutput = startDt.toLocaleString(DateTime.TIME_SIMPLE);
					break;

				default:
					dateOutput = startDt.toLocaleString(DateTime.DATETIME_MED);
			}
			break;

		case 'short': // 10/14/1983, 1:30 PM

			switch (mode) {
				case 'date':
					dateOutput = startDt.toLocaleString(DateTime.DATE_SHORT);
					break;

				case 'time':
					dateOutput = startDt.toLocaleString(DateTime.TIME_24_SIMPLE);
					break;

				default:
					dateOutput = startDt.toLocaleString(DateTime.DATETIME_SHORT);
			}
			break;

		case 'shorter': // 10/14 1:30 PM

			switch (mode) {
				case 'date':
					dateOutput = startDt.toLocaleString({
						month : 'numeric',
						day   : 'numeric',
					});
					break;

				case 'time':
					dateOutput = startDt.toLocaleString({
						hour   : 'numeric',
						minute : 'numeric',
					});
					break;

				default:
					dateOutput = startDt.toLocaleString({
						month  : 'numeric',
						day    : 'numeric',
						hour   : 'numeric',
						minute : 'numeric',
					});
			}

			dateOutput = dateOutput.replace(' AM', 'a');
			dateOutput = dateOutput.replace(' PM', 'p');
			break;

		case 'shortday': // Mon, Oct 14, 1:30P

			switch (mode) {
				case 'date':
					dateOutput = startDt.toLocaleString({
						weekday : 'short',
						month   : 'short',
						day     : 'numeric',
					});
					break;

				case 'time':
					dateOutput = startDt.toLocaleString(DateTime.TIME_SIMPLE);
					break;

				default:
					dateOutput = startDt.toLocaleString({
						weekday : 'short',
						month   : 'short',
						day     : 'numeric',
						hour    : 'numeric',
						minute  : 'numeric',
					})
						.replace(' AM', 'a')
						.replace(' PM', 'p');
			}
			break;

		default: // Oct 14, 1:30P

			switch (mode) {
				case 'date':
					dateOutput = startDt.toLocaleString({
						month   : 'short',
						day     : 'numeric',
					});
					break;

				case 'time':
					dateOutput = startDt.toLocaleString(DateTime.TIME_SIMPLE);
					break;

				default:
					dateOutput = startDt.toLocaleString({
						month  : 'short',
						day    : 'numeric',
						hour   : 'numeric',
						minute : 'numeric',
					})
						.replace(' AM', 'a')
						.replace(' PM', 'p');
			}
			break;
	}

	if (showFullTz) {
		dateOutput += ` ${startDt.toFormat('z')}`;
	} else if (showTz) {
		dateOutput += ` ${startDt.toFormat('ZZZZ')}`;
	}

</script>
	<span class="${spanClass}">
		{ dateOutput }
	</span>